age = 90

if age < 2:
    print("This person is a baby")
elif 2 <= age < 4:
    print("This person is a toddler")
elif 4 <= age < 13:
    print("This person is a child")
elif 13 <= age < 20:
    print("This person is a teenager")
elif 20 <= age < 65:
    print("This person is a adult")
else:
    print("This person is a elder")
